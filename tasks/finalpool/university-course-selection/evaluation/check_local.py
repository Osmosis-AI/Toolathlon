# Standard library imports for file system operations and data manipulation
import os
import re
import pandas as pd
from typing import Tuple, Optional
from itertools import permutations
from utils.general.helper import normalize_str


# Common Chinese academic title suffixes that may follow an instructor name
# in the source PDF.  Agents often copy the full ``<name> <title>`` cell
# verbatim, while the GT keeps only the name.  These are stylistic, not
# semantic — strip them on both sides before comparing.
_TITLE_TOKENS = (
    "青年副研究员", "青年研究员", "高级工程师", "教学为主型",
    "副教授", "副研究员", "助理研究员", "助理教授",
    "研究员", "工程师", "讲师", "教授",
)


def _strip_instructor_title(s: str) -> str:
    """Remove trailing title token (with optional parenthetical) from a
    Chinese instructor name like '陈彤兵 讲师' → '陈彤兵',
    '龚金平 教授(教学为主型)' → '龚金平'."""
    if s is None or (isinstance(s, float) and pd.isna(s)):
        return ""
    s = str(s).strip()
    # remove trailing parenthetical first: '教授(教学为主型)' / '教授（教学为主型）'
    s = re.sub(r'[（(][^）)]*[）)]\s*$', '', s).strip()
    for tok in _TITLE_TOKENS:
        # allow optional whitespace before the title at the end
        if s.endswith(tok):
            s = s[: -len(tok)].rstrip()
            break
    return s


# PDF program/quality tags that appear adjacent to enrollment in the
# course-schedule PDF but are NOT student-eligibility restrictions.
# Agents that copy them into the Restrictions cell should still match
# GT's '无' for unrestricted courses (the tags are category/quality
# markers, not eligibility filters).  Example tokens observed:
# '上海市精品课程团队', '复旦大学精品课程', '思政B'.
_NON_RESTRICTION_TAG_PATTERNS = (
    re.compile(r'^.*精品课程.*$'),
    re.compile(r'^思政[A-Za-z]$'),
)


def _restriction_tokens(s):
    """Tokenize a Course Selection Restrictions cell into a multiset of
    canonical fragments.

    Returns ``None`` for a missing/empty cell — distinct from an empty
    multiset, because the reference format ("若无限制则填入'无'") requires
    unrestricted courses to be filled with '无' explicitly, not left
    blank.  Returns ``frozenset()`` for '无'/'無' and for cells that
    contain only PDF program/quality tags (which are not real
    eligibility restrictions)."""
    if s is None or (isinstance(s, float) and pd.isna(s)):
        return None
    text = str(s).strip()
    if not text:
        return None
    if text in ("无", "無"):
        return frozenset()
    # Split on whitespace, full/half-width commas, semicolons, slashes.
    parts = re.split(r'[\s,，;；/\\]+', text)
    out = []
    for p in parts:
        p = p.strip().strip("()（）")
        if not p:
            continue
        # Drop standalone year-prefix tokens like '23'/'22级' that some
        # agents merge into a single phrase ('23 22 X' vs '23级 22级 X')
        # so the multiset matches regardless of whether the agent
        # attached the '级' suffix.
        p = re.sub(r'级$', '', p)
        # Drop PDF program/quality tags that aren't true restrictions.
        if any(pat.match(p) for pat in _NON_RESTRICTION_TAG_PATTERNS):
            continue
        out.append(p)
    return frozenset(out)


def check_local(agent_workspace: str, groundtruth_workspace: str, en_mode=True) -> Tuple[bool, Optional[str]]:
    """
    Main evaluation function for the university course selection task.

    This function compares Excel files generated by an AI agent against ground truth files
    to verify if the agent correctly completed the course selection task.

    Args:
        agent_workspace (str): Path to directory containing agent-generated Excel files
        groundtruth_workspace (str): Path to directory containing reference/expected Excel files

    Returns:
        Tuple[bool, Optional[str]]:
            - bool: True if agent output matches ground truth, False otherwise
            - Optional[str]: Success message or detailed error description
    """

    REQUIRED_COLUMNS = ['课程名称', '课程代码', '任课老师', '上课校区', '上课时间', '人数', '学分数', '考核方式', '考试时间', '选课限制条件']
    if en_mode:
        # Course name	Course ID	Instructor	Campus	Class Time	Enrollment	Credits	Assessment Method	Exam Time	Course Selection Restrictions
        REQUIRED_COLUMNS = ['Course name', 'Course ID', 'Instructor', 'Campus', 'Class Time', 'Enrollment', 'Credits', 'Assessment Method', 'Exam Time', 'Course Selection Restrictions']

    def clean_dataframe(df, cols):
        """Clean DataFrame, remove empty rows.

        Drops rows whose Course-ID key is missing — empty key = junk row.
        Per-cell emptiness (e.g. blank Restrictions) is handled by the
        column-level comparator, not here."""
        subset = df[cols].copy()
        subset = subset.dropna(how='all', subset=cols)
        # Key column varies by language mode
        key_candidates = [c for c in ("Course ID", "课程代码") if c in cols]
        if key_candidates:
            key = key_candidates[0]
            mask = subset[key].apply(lambda x: str(x).strip() != '' and not pd.isna(x))
            subset = subset[mask]
        return subset.reset_index(drop=True)

    # Columns that get field-specific canonicalization (rather than the
    # default normalize_str equality).  Each entry maps the column name
    # to a callable ``(val) -> comparable_key`` — values match iff the
    # keys are equal.  The point is to absorb known stylistic variation
    # between the agent's PDF-extracted text and the curated GT cell.
    INSTRUCTOR_COLS = {"Instructor", "任课老师"}
    RESTRICTIONS_COLS = {"Course Selection Restrictions", "选课限制条件"}

    def smart_compare(val1, val2, col=None):
        """Smart compare two values, handle numeric and string types.

        Caller pattern is ``smart_compare(gt_val, agent_val, col=...)``.
        """
        # Restrictions: order-independent multiset compare.  Reference
        # format requires '无' for unrestricted courses, so an empty
        # agent cell does NOT match.  PDF program/quality tags
        # (e.g. '上海市精品课程团队', '思政B') are stripped from both
        # sides because they aren't real eligibility restrictions.
        if col in RESTRICTIONS_COLS:
            gt_tokens = _restriction_tokens(val1)
            ag_tokens = _restriction_tokens(val2)
            if ag_tokens is None:
                # Agent left the cell empty — fails (reference requires '无').
                return False
            if gt_tokens is None:
                # GT itself is empty/malformed — treat any filled agent
                # value as acceptable (defensive; should not happen in
                # well-formed GTs).
                return True
            return gt_tokens == ag_tokens

        # Handle NaN for non-restriction columns
        if pd.isna(val1) and pd.isna(val2):
            return True
        if pd.isna(val1) or pd.isna(val2):
            return False

        # Try numeric comparison first
        try:
            num1 = float(str(val1).strip())
            num2 = float(str(val2).strip())
            return abs(num1 - num2) < 1e-10
        except (ValueError, TypeError):
            pass

        # Field-specific canonicalization before normalize_str equality.
        # Note: Class Time week-ranges ([1-16] etc.) are NOT stripped —
        # the reference format specifies '周x 第x-y节' exactly, so an
        # agent that leaves the PDF '[1-16]' artifact must be flagged.
        s1, s2 = str(val1).strip(), str(val2).strip()
        if col in INSTRUCTOR_COLS:
            s1 = _strip_instructor_title(s1)
            s2 = _strip_instructor_title(s2)
        return normalize_str(s1) == normalize_str(s2)

    def compare_single_files(gt_file_path, agent_file_path):
        """Compare a single groundtruth file and agent file"""
        try:
            # Read the groundtruth file, remove all empty sheets
            gt_excel = pd.ExcelFile(gt_file_path)
            gt_df = None
            for sheet_name in gt_excel.sheet_names:
                sheet_df = pd.read_excel(gt_file_path, sheet_name=sheet_name)
                # Check if the sheet is empty (all columns are empty or only contain empty strings)
                if not sheet_df.empty and not sheet_df.isna().all().all():
                    # Check if there are non-empty strings
                    has_content = False
                    for col in sheet_df.columns:
                        if sheet_df[col].astype(str).str.strip().ne('').any():
                            has_content = True
                            break
                    if has_content:
                        gt_df = sheet_df
                        break

            if gt_df is None:
                return False, "Groundtruth file does not contain any valid sheets"

            # Require ALL required columns to be present in GT (extras tolerated).
            missing_gt_cols = [c for c in REQUIRED_COLUMNS if c not in gt_df.columns]
            if missing_gt_cols:
                return False, f"Groundtruth file missing required columns: {missing_gt_cols}"

            gt_cleaned = clean_dataframe(gt_df, REQUIRED_COLUMNS)
            if gt_cleaned.empty:
                return False, "Groundtruth file does not contain any valid data"

            # Read the agent file, remove all empty sheets
            agent_excel = pd.ExcelFile(agent_file_path)
            agent_df = None
            for sheet_name in agent_excel.sheet_names:
                sheet_df = pd.read_excel(agent_file_path, sheet_name=sheet_name)
                # Check if the sheet is empty (all columns are empty or only contain empty strings)
                if not sheet_df.empty and not sheet_df.isna().all().all():
                    # Check if there are non-empty strings
                    has_content = False
                    for col in sheet_df.columns:
                        if sheet_df[col].astype(str).str.strip().ne('').any():
                            has_content = True
                            break
                    if has_content:
                        agent_df = sheet_df
                        break

            if agent_df is None:
                return False, "Agent file does not contain any valid sheets"

            # Require ALL required columns to be present in agent file (extras tolerated).
            missing_agent_cols = [c for c in REQUIRED_COLUMNS if c not in agent_df.columns]
            if missing_agent_cols:
                return False, f"Agent file missing required columns: {missing_agent_cols}"

            agent_cleaned = clean_dataframe(agent_df, REQUIRED_COLUMNS)
            if agent_cleaned.empty:
                return False, "Agent file does not contain any valid data"

            # Compare against the full required column set.
            common_cols = REQUIRED_COLUMNS

            # Check if the number of data rows is consistent
            if len(gt_cleaned) != len(agent_cleaned):
                return False, f"Data row count mismatch - GT: {len(gt_cleaned)} rows, Agent: {len(agent_cleaned)} rows"

            # Align rows by the Course ID key (deterministic across
            # PDF-extraction variants).  Falls back to sorting on all
            # common columns if neither key column is present.
            sort_key = next((c for c in ("Course ID", "课程代码") if c in common_cols), None)
            if sort_key:
                gt_sorted = gt_cleaned[common_cols].sort_values(by=sort_key).reset_index(drop=True)
                agent_sorted = agent_cleaned[common_cols].sort_values(by=sort_key).reset_index(drop=True)
            else:
                gt_sorted = gt_cleaned[common_cols].sort_values(by=common_cols).reset_index(drop=True)
                agent_sorted = agent_cleaned[common_cols].sort_values(by=common_cols).reset_index(drop=True)

            # Compare the content of the specified columns row by row
            mismatches = []
            for i in range(len(gt_sorted)):
                for col in common_cols:
                    gt_val = gt_sorted.iloc[i][col]
                    agent_val = agent_sorted.iloc[i][col]

                    if not smart_compare(gt_val, agent_val, col=col):
                        mismatches.append({
                            'row': i + 1,
                            'column': col,
                            'groundtruth': str(gt_val),
                            'agent': str(agent_val)
                        })

            if mismatches:
                error_msg = f"Found {len(mismatches)} mismatches"
                return False, error_msg

            return True, f"Successfully matched {len(gt_sorted)} valid records"

        except Exception as e:
            return False, f"Error when comparing files: {str(e)}"

    # Get the groundtruth file list
    groundtruth_files = []
    for file in os.listdir(groundtruth_workspace):
        if file.endswith('.xlsx'):
            groundtruth_files.append(file)

    if not groundtruth_files:
        return False, "No Excel files found in groundtruth workspace"

    # Get the agent file list
    agent_files = []
    for file in os.listdir(agent_workspace):
        if file.endswith('.xlsx'):
            agent_files.append(file)

    if not agent_files:
        return False, "No Excel files found in agent workspace"

    # Check if the number of files is equal
    if len(groundtruth_files) != len(agent_files):
        return False, f"File count mismatch - Groundtruth: {len(groundtruth_files)} files, Agent: {len(agent_files)} files"

    print(f"Start finding the perfect matching solution, need to check {len(groundtruth_files)}! = {len(list(permutations(agent_files)))} permutations")

    # Try all possible agent file permutations, find the perfect matching solution
    for agent_permutation in permutations(agent_files):
        print(f"Trying permutation: {list(zip(groundtruth_files, agent_permutation))}")

        # Check if the current permutation can achieve a perfect match
        all_matched = True
        match_details = []

        for gt_file, agent_file in zip(groundtruth_files, agent_permutation):
            gt_file_path = os.path.join(groundtruth_workspace, gt_file)
            agent_file_path = os.path.join(agent_workspace, agent_file)

            success, message = compare_single_files(gt_file_path, agent_file_path)

            if success:
                match_details.append(f"{gt_file} ↔ {agent_file}: {message}")
            else:
                print(f"  Failed: {gt_file} vs {agent_file} - {message}")
                all_matched = False
                break  # The current permutation failed, try the next permutation

        if all_matched:
            # Find the perfect matching solution
            success_msg = f"Found the perfect matching solution!\n"
            for detail in match_details:
                success_msg += f"  {detail}\n"
            return True, success_msg.strip()

    # If all permutations cannot achieve a perfect match
    return False, f"Tried all {len(list(permutations(agent_files)))} permutations, but cannot find the perfect matching solution"
